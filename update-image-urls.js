const fs = require('fs');
const path = require('path');

// Supabase Storage URLs
const SUPABASE_BASE_URL = 'https://srpspzgemnfxkqalgjmz.supabase.co/storage/v1/object/public/wolf-property-images';

const IMAGE_URL_MAPPING = {
    'images/wolf-logo.png': `${SUPABASE_BASE_URL}/wolf-logo.png`,
    'images/people/adam_starr.png': `${SUPABASE_BASE_URL}/people/adam_starr.png`,
    'images/people/patricia_holmes.jpg': `${SUPABASE_BASE_URL}/people/patricia_holmes.jpg`
};

function updateFile(filePath, replacements) {
    try {
        let content = fs.readFileSync(filePath, 'utf8');
        let updated = false;

        Object.entries(replacements).forEach(([oldPath, newUrl]) => {
            const regex = new RegExp(oldPath.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g');
            if (content.includes(oldPath)) {
                content = content.replace(regex, newUrl);
                updated = true;
                console.log(`  âœ“ Updated ${oldPath} -> ${newUrl}`);
            }
        });

        if (updated) {
            fs.writeFileSync(filePath, content, 'utf8');
            console.log(`âœ… Updated ${filePath}`);
        } else {
            console.log(`â­ï¸  No changes needed in ${filePath}`);
        }

        return updated;
    } catch (error) {
        console.error(`âŒ Error updating ${filePath}:`, error.message);
        return false;
    }
}

function updateImageUrls() {
    console.log('ðŸ”„ Updating image URLs to use Supabase Storage...\n');

    const filesToUpdate = [
        'index.html',
        'about.html',
        'setup-database.sql',
        'complete-database-setup.sql'
    ];

    let totalUpdated = 0;

    filesToUpdate.forEach(fileName => {
        console.log(`ðŸ“ Processing ${fileName}...`);
        if (fs.existsSync(fileName)) {
            const wasUpdated = updateFile(fileName, IMAGE_URL_MAPPING);
            if (wasUpdated) totalUpdated++;
        } else {
            console.log(`âš ï¸  File ${fileName} not found, skipping...`);
        }
        console.log('');
    });

    console.log('ðŸ“Š Summary:');
    console.log(`âœ… ${totalUpdated} files updated`);
    console.log(`ðŸ–¼ï¸  ${Object.keys(IMAGE_URL_MAPPING).length} image paths replaced`);
    
    console.log('\nðŸŒ New Image URLs:');
    Object.entries(IMAGE_URL_MAPPING).forEach(([local, url]) => {
        console.log(`  ${local} -> ${url}`);
    });

    console.log('\nâœ¨ Update complete! Your website now uses Supabase Storage for images.');
}

